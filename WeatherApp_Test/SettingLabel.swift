import Foundation
import UIKit
import SnapKit


public func settingLabel(_ cityLabel: UILabel) {
    cityLabel.textAlignment = .center
    cityLabel.textColor = .black
    cityLabel.font = UIFont(name: "Helvetica-Bold", size: 23)
}
